#include <iostream>
using namespace std;
int main(){
    int num1,num2;
    // <<    this is called insertion operator
    // >>    this is called extraction operator 
    cout<<"enter the value of num1: "<<endl;
    cin>>num1;

    cout<<"enter the value of num2: "<<endl;
    cin>>num2;

    cout<<"the sum is "<<num1+num2;
}